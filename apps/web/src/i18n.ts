export type Locale = keyof typeof messages;
export type MessageKey = keyof (typeof messages)["en"];

export function resolveLocale(uiLanguage: UiLanguage, navigatorLanguage?: string | null): Locale {
  if (uiLanguage === "en" || uiLanguage === "zh") {
    return uiLanguage;
  }

  const language = navigatorLanguage ?? "";
  return ZH_LANGUAGE_PREFIXES.some(
    (prefix) => language === prefix || language.startsWith(`${prefix}-`),
  )
    ? "zh"
    : "en";
}

export function interpolate(message: string, values?: Readonly<Record<string, string | number>>) {
  if (!values) {
    return message;
  }
  return message.replace(/\{(\w+)\}/g, (match, key) => String(values[key] ?? match));
}

export function useLocale(): Locale {
  const uiLanguage = useSettings((settings) => settings.uiLanguage);
  return resolveLocale(uiLanguage, typeof navigator === "undefined" ? null : navigator.language);
}

export function useT() {
  const locale = useLocale();
  return useMemo(
    () => (key: MessageKey, values?: Readonly<Record<string, string | number>>) =>
      interpolate(messages[locale][key] ?? messages.en[key], values),
    [locale],
  );
}
